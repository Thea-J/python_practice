# Assign a list to variable x
x = [1, 2, 3, 4]

# Use square-bracket notation to retrieve list element
# Note: list indices start at 0 
y = x[0]

# Use square-bracket notation to modify a list element
x[2] = 7

# Concatenate 2 lists 
